    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            int dialogOption = JOptionPane.showConfirmDialog(null, " Would you like to delete selected Person?", "", JOptionPane.YES_NO_OPTION);
            if (dialogOption == JOptionPane.YES_OPTION) {
                Employee p = (Employee) jTable1.getValueAt(selectedRow, 0);
                personList.removeEmployee(p);
                populateTable();
                //resetFields();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row from table", "Warning", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:

        int selectedRow1 = jTable1.getSelectedRow();

        if (selectedRow1 >= 0) {
            Employee p = (Employee) jTable1.getValueAt(selectedRow1, 0);
            ViewPersonDetailsJPanel vpdjp = new ViewPersonDetailsJPanel(userProcessContainer, p);
            userProcessContainer.add(vpdjp);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row first !", "Error Message", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnViewDetailsActionPerformed

