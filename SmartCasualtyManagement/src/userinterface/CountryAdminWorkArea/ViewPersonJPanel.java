    private void BtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSearchActionPerformed
        // TODO add your handling code here:
        String search = txtSearch.getText();

        if (search == null || search.length() == 0) {
            JOptionPane.showMessageDialog(null, "Please provide a valid person ID", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Employee result = personList.searchEmployee(search);
            if (result != null) {
                SearchJPanel sjp = new SearchJPanel(userProcessContainer, result);
                userProcessContainer.add("Search Results", sjp);
                CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
                cardLayout.next(userProcessContainer);

                populateTable();

            } else {
                JOptionPane.showMessageDialog(null, "Person doesn't exist", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }


    }//GEN-LAST:event_BtnSearchActionPerformed


