    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            try {

                int rrate = Integer.parseInt(TxtRespRate.getText());
                int hrate = Integer.parseInt(txthrate.getText());
                int bp = Integer.parseInt(Txtbp.getText());
                float wt = Float.parseFloat(txtWT.getText());

                if (rrate > 0 && hrate > 0 && bp > 0 && wt > 0.0) {
                    // System.out.println("the patient name is :"+p.getPersonName());

                    //Person person = (Person) selectPatComboBox.getSelectedItem();
                    VitalSign vs = (VitalSign) jTable1.getValueAt(selectedRow, 0);

                    vs.setRespRate(rrate);
                    vs.setHeartRate(hrate);
                    vs.setBloodPressure(bp);
                    vs.setWeight(wt);
                    vs.getTimestamp();

                    populateTable();

                    //Display success message
                    JOptionPane.showMessageDialog(null, "Vital Sign sucessfully updated", "Add Success", JOptionPane.INFORMATION_MESSAGE);
                    resetFields();
                } else {
                    JOptionPane.showMessageDialog(null, "Enter a valid input", "Invalid Input", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Only numbers accepted.Please enter valid values", "Invalid input", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_btnSaveActionPerformed
        editPermission(false);
    }


