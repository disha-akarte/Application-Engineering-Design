    private void displayDetails() {
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow >= 0) {
            VitalSign vs = (VitalSign) jTable1.getValueAt(selectedRow, 0);
            TxtRespRate.setText(String.valueOf(vs.getRespRate()));
            txthrate.setText(String.valueOf(vs.getHeartRate()));
            Txtbp.setText(String.valueOf(vs.getBloodPressure()));
            txtWT.setText(String.valueOf(vs.getWeight()));
            txtTimestamp.setText(vs.getTimestamp());

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row first !", "Error Message", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void resetFields() {
        TxtRespRate.setText("");
        txthrate.setText("");
        Txtbp.setText("");
        txtWT.setText("");

        txtTimestamp.setText("");

    }

    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            int dialogOption = JOptionPane.showConfirmDialog(null, " Would you like to delete selected VitalSign?", "", JOptionPane.YES_NO_OPTION);
            if (dialogOption == JOptionPane.YES_OPTION) {
                Person person = (Person) selectPatComboBox.getSelectedItem();
                VitalSign vs = (VitalSign) jTable1.getValueAt(selectedRow, 0);
                person.getVitalSignHistory().deleteVitalSign(vs);
                populateTable();
                resetFields();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row from table", "Warning", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_BtnDeleteActionPerformed

