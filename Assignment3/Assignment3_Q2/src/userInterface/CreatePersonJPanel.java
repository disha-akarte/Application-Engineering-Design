    private void btncreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncreateActionPerformed
        // TODO add your handling code here:

        // TODO add your handling code here:
        Person person = personList.addPerson();

        String pname = txtName.getText().trim();
        String padd = txtAdd.getText().trim();
        String pid = txtpatID.getText().trim();

        if (!pname.isEmpty() && !padd.isEmpty()) {
            try {
                int pAge = Integer.parseInt(txtAge.getText().trim());
                if (pAge > 0 && pAge <= 150) {
                    person.setAge(pAge);
                } else {
                    JOptionPane.showMessageDialog(null, "Enter a valid age in the range 1 to 150!", "Error message", JOptionPane.ERROR_MESSAGE);
                    return;
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Enter valid input for Patient Age", "Invalid Data", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (pname.matches("[A-Za-z]+[\\s]([A-Za-z])+|[A-Za-z]+")) {
                person.setPersonName(pname);
            } else {
                JOptionPane.showMessageDialog(null, "Please enter correct Patient Name", "Error", JOptionPane.PLAIN_MESSAGE);
                return;
            }
            if (pid.matches("[A-Za-z0-9]+")) {
                person.setPersonID(pid);
            } else {
                JOptionPane.showMessageDialog(null, "Please enter proper Patient ID", "Error", JOptionPane.PLAIN_MESSAGE);
                return;
            }

            if (padd.matches("[A-Za-z]+[\\s]([A-Za-z])+|[A-Za-z]+")) {
                person.setPersonAddress(padd);
            } else {
                JOptionPane.showMessageDialog(null, "Please enter valid Address", "Error", JOptionPane.PLAIN_MESSAGE);
                return;
            }

            JOptionPane.showMessageDialog(null, "Person successfully added", "Information", JOptionPane.INFORMATION_MESSAGE);
            resetFields();
        } else {
            JOptionPane.showMessageDialog(null, "Enter proper values");
        }

    }

    private void resetFields() {
        txtAdd.setText("");
        txtName.setText("");
        txtAge.setText("");
        txtpatID.setText("");

    }//GEN-LAST:event_btncreateActionPerformed

