    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        // drugIt = store.getDrugInventory().addDrugItem();

        if (!txtAddQuantity.getText().isEmpty() && !txtDrugSerial.getText().isEmpty()
                && !txtExpiry.getText().isEmpty()) {

            if(txtExpiry.getText().matches("(?:0[1-9]|1[0-2])/[0-9]{2}"))
            {
                int q= Integer.parseInt(txtAddQuantity.getText().trim());
                if( q> 0 && q<=100){
                drugIt.setQuantity(String.valueOf(q));
                drugIt.setExpiration_date(txtExpiry.getText());
                drugIt.setDrug((Drug) DrugComboBox.getSelectedItem());
                JOptionPane.showMessageDialog(this, "Drug Item added successfully!");
                
                resetFields();
                
                DrugComboBox.setEnabled(false);
                btnBack.setEnabled(true);
                btnAddMore.setEnabled(true);
                btnSave.setEnabled(false);
                btnCancel.setEnabled(false);
            }
                else{
                   
                    JOptionPane.showMessageDialog(this,"Quantity should be between 1 to 100");
                }
            }
            else{
                JOptionPane.showMessageDialog(this,"Enter proper value for Expiry Date in the format MM/YY");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Text fields cannt be blank");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

