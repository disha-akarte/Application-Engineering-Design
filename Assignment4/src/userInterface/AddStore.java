    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        String sArea = txtArea.getText().trim();
        String sID = txtStoreID.getText().trim();
        String sZip = txtZip.getText();

        if (!sArea.isEmpty() && !sID.isEmpty() && !sZip.isEmpty()) {

            if (sID.matches("^[a-zA-Z0-9]{3,8}$")) {

                if (sArea.matches("([A-Za-z]+[\\s]([A-Za-z])+)|[A-Za-z]+")) {

                    if (sZip.matches("^[0-9]{5}(?:-[0-9]{4})?$")) {

                        Store store = storeDirectory.addStore();
                        store.setStoreID(sID);
                        store.setStoreArea(sArea);
                        store.setZipCode(sZip);

                        JOptionPane.showMessageDialog(null, "Store added successfully", "Info", JOptionPane.INFORMATION_MESSAGE);
                        resetFields();

                    } else {
                        JOptionPane.showMessageDialog(null, "Please enter correct zip code", "Error", JOptionPane.PLAIN_MESSAGE);
                   //     return;
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Please enter a valid value", "Error", JOptionPane.PLAIN_MESSAGE);
                 //   return;
                }
            } else {
                JOptionPane.showMessageDialog(null, "ID can be minimum 3 and maximum 8 characters", "Error", JOptionPane.PLAIN_MESSAGE);
               // return;
            }

        } else {
            JOptionPane.showMessageDialog(this, "The Fields cannot be blank");
        }


    }//GEN-LAST:event_btnAddActionPerformed

