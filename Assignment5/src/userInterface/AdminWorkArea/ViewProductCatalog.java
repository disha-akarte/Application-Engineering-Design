    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        int key = Integer.parseInt(txtPIDSearch.getText());

        Product product = productcatalog.searchProduct(key);
        if(product==null){
            JOptionPane.showMessageDialog(this,"Product doesn't exist");
        }
        else{
        ViewProductDetailsJPanel vpdjp = new ViewProductDetailsJPanel(userProcessContainer, product);
        userProcessContainer.add("View Searched Product", vpdjp);
        CardLayout cl = (CardLayout) userProcessContainer.getLayout();
        cl.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:

        int row = tblProducts.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            Product product = (Product) tblProducts.getValueAt(row, 0);
            ViewProductDetailsJPanel vpdjp = new ViewProductDetailsJPanel(userProcessContainer, product);
            userProcessContainer.add("Add Product", vpdjp);
            CardLayout cl = (CardLayout) userProcessContainer.getLayout();
            cl.next(userProcessContainer);
        }

    }//GEN-LAST:event_btnViewActionPerformed


