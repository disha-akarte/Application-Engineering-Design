    void populateTable() {

        DefaultTableModel dtm = (DefaultTableModel) tblProducts.getModel();
        dtm.setRowCount(0);
        for (Product p : productcatalog.getProductcatalog()) {
            Object row[] = new Object[4];
            row[0] = p;
            row[1]= p.getFloor();
            row[2] = p.getPrice();
            row[3]= p.getCeil();
            dtm.addRow(row);
        }

    }

