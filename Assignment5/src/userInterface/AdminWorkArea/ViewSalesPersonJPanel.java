    void populateTable() {

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for (Salesperson s : salesPersonDirectory.getSalesPersonDirectory()) {
            Object row[] = new Object[2];
            row[0] = s;
            row[1] = s.getPersonName();
            dtm.addRow(row);
        }

    }

