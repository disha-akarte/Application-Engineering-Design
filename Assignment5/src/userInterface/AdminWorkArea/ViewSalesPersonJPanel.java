    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String key = txtSPID.getText();
        int search = Integer.parseInt(txtSPID.getText());
        if(key== null || key.length()==0){
            JOptionPane.showMessageDialog(null, "Please provide a valid salesperson ID", "Warning", JOptionPane.WARNING_MESSAGE);
        } 
        else {
            Salesperson s = salesPersonDirectory.searchSalesperson(search);
            if (s != null) {

                SearchSPJPanel mspjp = new SearchSPJPanel(userProcessContainer, s);
                userProcessContainer.add("Search SP", mspjp);
                CardLayout cl = (CardLayout) userProcessContainer.getLayout();
                cl.next(userProcessContainer);

            } else {
                JOptionPane.showMessageDialog(null, "SalesPerson doesn't exist", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }

        System.out.print("key from search box is :" + key);

    }//GEN-LAST:event_btnSearchActionPerformed


