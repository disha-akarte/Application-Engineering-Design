    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        final Pattern pattern = Pattern.compile("^[A-Za-z, ]++$");

        if (!pattern.matcher(txtSPName.getText()).matches()) {
            JOptionPane.showMessageDialog(null, "Cannot add invalid values. Please input only alphabets", "Caution", JOptionPane.ERROR_MESSAGE);
            return;

        } else {       
        String name = txtSPName.getText();
        try{
        int salesID = Integer.parseInt(txtSPID.getText());
        
        salesPerson = salesPersonDirectory.addSalesperson();
        salesPerson.setPersonName(name);
        salesPerson.setSalesID(salesID);
        JOptionPane.showMessageDialog(this,"Salesperson added succesfully");
        txtSPID.setText("");
        txtSPName.setText("");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,"Only numbers accepted for the Sales ID field");
        }
        }


    }//GEN-LAST:event_btnSaveActionPerformed


