    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        //to check if no invalid data is entered in patient age field
        String pname = txtpatname.getText().trim();
        String pid = txtpatID.getText().trim();
        String pDoc = txtpriDocname.getText().trim();
        String pPharm = txtprefPharm.getText().trim();
        
        if( !pname.isEmpty() && !pid.isEmpty() 
                && !pDoc.isEmpty() && !pPharm.isEmpty()){
        try{
            
            int pAge = Integer.parseInt(txtpatAge.getText());
            if(pAge> 0 && pAge<=150)
            pat.setPatientAge(pAge);
          
            else{
                JOptionPane.showMessageDialog(null, "Enter a valid age in the range 1 to 150!","Error message", JOptionPane.ERROR_MESSAGE);
                return;
            }
           
        }catch(NumberFormatException e){
         JOptionPane.showMessageDialog(null, "Enter valid input for Patient Age", "Invalid Data", JOptionPane.ERROR_MESSAGE);
         return;
        }
        
        //to validate that no blank fields are excepted  
        
            if(pname.matches("[A-Za-z]+[\\s]([A-Za-z])+|[A-Za-z]+"))
            pat.setPatientName(pname);
            
            else{
                JOptionPane.showMessageDialog(null,"Please enter correct Patient Name", "Error", JOptionPane.PLAIN_MESSAGE );
                return;
            }
            if(pid.matches("[A-Za-z0-9]+"))
            pat.setPatientID(pid);
            else{
                JOptionPane.showMessageDialog(null,"Please enter proper Patient ID", "Error", JOptionPane.PLAIN_MESSAGE );
                return;
            }
                
            if(pDoc.matches("[A-Za-z]+[\\s]([A-Za-z])+|[A-Za-z]+"))
            pat.setPrimaryDoctor(pDoc);
            else{
                JOptionPane.showMessageDialog(null,"Please enter valid Doctor Name", "Error", JOptionPane.PLAIN_MESSAGE );
                return;
            }
            if(pPharm.matches("[A-Za-z]+[\\s]([A-Za-z])+|[A-Za-z]+"))
            pat.setPrefPharmacy(pPharm);
            else{
                JOptionPane.showMessageDialog(null,"Please enter Pharmacy Name", "Error", JOptionPane.PLAIN_MESSAGE );
                return;
            }
            JOptionPane.showMessageDialog(null, "Congratulations! Patient has been successfully added", "SUCCESS", JOptionPane.PLAIN_MESSAGE );
            resetFields();
                   
    }//GEN-LAST:event_btnSaveActionPerformed
      else
         JOptionPane.showMessageDialog(null, "All fields are mandatory","Missing fields",JOptionPane.ERROR_MESSAGE);  
    }
